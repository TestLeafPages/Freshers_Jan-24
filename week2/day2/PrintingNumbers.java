package week2.day3;

public class PrintingNumbers {

	public static void main(String[] args) {

		// for (initialization;Condition;increament or decreament){
		// }
		for (int i = 0; i < 5; i++) {

			System.out.println(i);
		}

		System.out.println("Second Iteration");
		for (int i = 5; i >0; i--) {

			System.out.println(i);
		}

	}
}